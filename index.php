<h1>Avis aux amateurs de street-art : le Cabaret du Chat Noir dévoile 3 000 m2 de créations et réunit 350 artistes dans son Expo 150 ! </h1>
 <p>Au cœur d'un ancien central téléphonique, les graffitis et les dessins se mêlent aux peintures à la bombe et aux œuvres chimériques. Véritable scène ouverte, le Chat Noir a donné carte blanche et une liberté complète aux artistes afin de transcender ce lieu en un espace d'art, de partage et de contemporanéité. Digne héritier du cabaret du XIXe siècle, le Chat Noir s'inscrit dans cette célébration des artistes indépendants et souvent mis de côté dans les institutions. Le street-art peut s'épanouir ici, occuper son espace et s'en emparer pour le sublimer. La myriade impressionnante de créateurs réunis pour l'occasion promet un spectacle grisant : une transdisciplinarité comme mot d'ordre et une cascade de styles, de couleurs et de voix différentes comme tableau. Ici, il faut regarder partout, prêter attention aux murs, aux plafonds et aux parterres, peuplés d'anamorphoses vertigineuses. Pour consacrer la dynamique de l'exposition entièrement gratuite, différentes activités prendront place et participeront à enrichir l'esprit du lieu, et atelier de poésie, DJ-set et performances rythmeront les soirées de cet espace incroyable.</p>
