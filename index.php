Dossier de presse – Le Salon des Beaux Arts 2024
Les 160 ans du Salon des Beaux Arts : le Salon d’hiver !
Pendant deux semaines, du 4 au 15 décembre 2024 au Réfectoire des Cordeliers Paris 6e, le Salon des Beaux Arts vous invite à découvrir les œuvres contemporaines d’artistes venu·e·s du monde entier dans les sections Naturaliste, Papier-Papiers, Peinture, Photographie, Sculpture et Textile.

Créé en 1864, par des artistes pour des artistes, la vocation du Salon est de soutenir la création, de mettre en lumière des talents et permettre l’envol d’artistes émergent·e·s. Le Salon est ainsi un soutien unique à la création et un outil indispensable permettant aux talents d’affirmer leur indépendance et leur liberté.

Premier Salon à avoir accueilli les artistes étrangers (Brancusi, Foujita ou Zao Wou-Ki) et les artistes femmes (Suzanne Valadon, Pān Yùliáng, Camille Claudel), le Salon des Beaux Arts s’engage également, depuis l’origine, sur des partenariats originaux, qui font sa force et sa singularité. 
Cette année encore, de nombreux prix partenaires viendront doter des artistes exposé·e·s.</p>
